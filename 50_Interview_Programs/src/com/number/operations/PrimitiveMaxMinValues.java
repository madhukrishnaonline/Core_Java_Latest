package com.number.operations;

public class PrimitiveMaxMinValues {

	public static void main(String[] args) {
		System.out.println("byte");
		byte min = Byte.MIN_VALUE;
		byte max = Byte.MAX_VALUE;
		System.out.println("Min Value :: " + min);
		System.out.println("Max Value :: " + max);

		System.out.println("short");
		short shortMin = Short.MIN_VALUE;
		short shortMax = Short.MAX_VALUE;
		System.out.println("Min Value :: " + shortMin);
		System.out.println("Max Value :: " + shortMax);

		System.out.println("int");
		int intMin = Integer.MIN_VALUE;
		int intMax = Integer.MAX_VALUE;
		System.out.println("Min Value :: " + intMin);
		System.out.println("Max Value :: " + intMax);

		System.out.println("long");
		long longMin = Long.MIN_VALUE;
		long longMax = Long.MAX_VALUE;
		System.out.println("Min Value :: " + longMin);
		System.out.println("Max Value :: " + longMax);
		/*==Long Max Value==
		 Nine quintillion, 
		two hundred twenty-three quadrillion, 
		three hundred seventy-two trillion, 
		thirty-six billion, 
		eight hundred fifty-four million, 
		seven hundred seventy-five thousand, 
		eight hundred seven.
		*/

		System.out.println("float");
		float floatMin = Float.MIN_VALUE;
		float floatMax = Float.MAX_VALUE;
		System.out.println("Min Value :: " + floatMin);
		System.out.println("Max Value :: " + floatMax);

		System.out.println("double");
		double doubleMin = Double.MIN_VALUE;
		double doubleMax = Double.MAX_VALUE;
		System.out.println("Min Value :: " + doubleMin);
		System.out.println("Max Value :: " + doubleMax);

		System.out.println("char");
		char charMin = Character.MIN_VALUE;
		char charMax = Character.MAX_VALUE;
		System.out.println("Min Value :: " + charMin);
		System.out.println("Max Value :: " + charMax);
	}// main
}
